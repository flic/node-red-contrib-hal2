'use strict';
// Generates docs/API.md from the single-source tool catalog (core/mcp-tools.js),
// so the hal2Api documentation never drifts from what the runtime actually exposes.
// Run with:  npm run docs:api
const fs   = require('fs');
const path = require('path');
const {
    MCP_TOOLS, MCP_TOOLS_ADMIN, TOOL_HARDWARE_REQUIREMENTS
} = require('../core/mcp-tools');

function sampleValue(prop) {
    if (Array.isArray(prop.enum) && prop.enum.length) return JSON.stringify(prop.enum[0]);
    switch (prop.type) {
        case 'number':
        case 'integer': return '0';
        case 'boolean': return 'true';
        case 'array':   return '[]';
        case 'object':  return '{}';
        default:        return '"…"';
    }
}

function paramsTable(schema) {
    const props = (schema && schema.properties) || {};
    const names = Object.keys(props);
    if (!names.length) return '_No parameters._\n';
    const required = new Set((schema && schema.required) || []);
    let out = '| Parameter | Type | Required | Description |\n|---|---|---|---|\n';
    for (const name of names) {
        const p = props[name];
        const type = p.enum ? p.enum.map(v => '`' + v + '`').join(' \\| ')
                            : '`' + (p.type || 'any') + '`';
        const desc = (p.description || '').replace(/\|/g, '\\|');
        out += `| \`${name}\` | ${type} | ${required.has(name) ? 'yes' : 'no'} | ${desc} |\n`;
    }
    return out;
}

function exampleRequest(tool) {
    const props = (tool.inputSchema && tool.inputSchema.properties) || {};
    const required = new Set((tool.inputSchema && tool.inputSchema.required) || []);
    // Prefer required params; otherwise show up to two representative ones.
    let keys = Object.keys(props).filter(k => required.has(k));
    if (!keys.length) keys = Object.keys(props).slice(0, 2);
    const args = keys.map(k => `"${k}": ${sampleValue(props[k])}`).join(', ');
    return `{ "tool": "${tool.name}", "args": { ${args} } }`;
}

function section(tool, isAdmin) {
    let md = `### \`${tool.name}\`${isAdmin ? ' 🔒 (admin)' : ''}\n\n`;
    md += tool.description.trim() + '\n\n';
    const hw = TOOL_HARDWARE_REQUIREMENTS[tool.name];
    if (hw) md += `> **Requires hardware:** at least one item of type ${hw.map(t => '`' + t + '`').join(', ')} at this location.\n\n`;
    if (isAdmin) md += `> Exposed through hal2Api only when **Allow admin tools** is enabled on the node.\n\n`;
    md += '**Parameters**\n\n' + paramsTable(tool.inputSchema) + '\n';
    md += '**Example**\n\n```json\n' + exampleRequest(tool) + '\n```\n\n';
    return md;
}

function build() {
    const toc = [...MCP_TOOLS, ...MCP_TOOLS_ADMIN]
        .map(t => `- [\`${t.name}\`](#${t.name.replace(/_/g, '')})`).join('\n');

    let md = '';
    md += '<!-- AUTO-GENERATED by scripts/gen-api-docs.js — do not edit by hand. Run `npm run docs:api`. -->\n\n';
    md += '# hal2 JSON API\n\n';
    md += 'The **hal2Api** node exposes the hal2 tool catalog as a JSON request/response gateway. ';
    md += 'Wire it behind an `http in`, MQTT, or any node that produces a JSON message.\n\n';
    md += '## Contract\n\n';
    md += '**Request** — set `msg.payload`:\n\n';
    md += '```json\n{ "tool": "<name>", "args": { /* tool-specific */ } }\n```\n\n';
    md += '`msg.tool` / `msg.args` override the payload fields when present. ';
    md += 'An optional `msg.claims` object is forwarded to the tool (used for admin role checks).\n\n';
    md += '**Response** — `msg.payload`:\n\n';
    md += '```json\n{ "ok": true, "result": /* tool output */ }\n```\n\n';
    md += 'On failure:\n\n```json\n{ "ok": false, "error": { "code": -32601, "message": "…" } }\n```\n\n';
    md += 'Most tools return a JSON object in `result`. A few admin tools return plain text.\n\n';
    md += '## Tools\n\n' + toc + '\n\n';
    for (const t of MCP_TOOLS)       md += section(t, false);
    md += '## Admin tools\n\n';
    md += 'These require **Allow admin tools** to be enabled on the hal2Api node.\n\n';
    for (const t of MCP_TOOLS_ADMIN) md += section(t, true);
    return md;
}

const outDir  = path.join(__dirname, '..', 'docs');
const outFile = path.join(outDir, 'API.md');
fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(outFile, build(), 'utf8');
console.log('Wrote ' + path.relative(path.join(__dirname, '..'), outFile) +
    ' (' + (MCP_TOOLS.length + MCP_TOOLS_ADMIN.length) + ' tools)');
